import UIKit

extension UIColor {
    class func synchronyYellow() -> UIColor {
        return UIColor(red: 248.0/255.0, green: 193.0/255.0, blue: 10.0/255.0, alpha: 1)
    }
    
    class func synchronyDarkGrey() -> UIColor {
        return UIColor(red: 51.0/255.0, green: 51.0/255.0, blue: 51.0/255.0, alpha: 1)
    }
    
    class func synchronyGreen() -> UIColor {
        return UIColor(red: 74.0/255.0, green: 157.0/255.0, blue: 31.0/255.0, alpha: 1)
    }
    
    class func synchronyLightGrey() -> UIColor {
        return UIColor(red: 127.0/255, green: 127.0/255, blue: 127.0/255, alpha: 1)
    }
}